"""Pack the §9E.1 C3-full Colab bundle.

Contents (src + ckpt + benchmark):
  src/*.py                              — all modules C3-full transitively uses
  results/sbi_prior_emit/density_estimator_last.pt   — SBI w_4 ckpt (§9C.2 v4)
  results/sbi_prior_emit/hand_cases_v1.jsonl         — emission for prior
  data/benchmark/echem_reason_benchmark.jsonl         — 128 QA
  prompts/sbi_prior_schema_v1.json + prompts/sbi_prior_config_v1.md — for sbi_prior_emit

Writes pvgap_9E1_c3full.zip in colab/. Expected ~400 KB.
"""
from __future__ import annotations
import zipfile
from pathlib import Path

HERE = Path(__file__).resolve().parent
ROOT = HERE.parent
OUT = HERE / "pvgap_9E1_c3full.zip"

FILES = [
    # --- src ---
    "src/__init__.py",
    "src/run_9e_pilot.py",
    "src/run_9e_main.py",
    "src/summarise_9e_main.py",
    "src/llm_judge.py",
    "src/sbi_prior_emit.py",
    "src/scenario_bridge.py",
    "src/candidate_param_extractor.py",  # v3 fix (2026-04-22)
    "src/weaver_signals.py",
    "src/pybamm_eis_residual.py",
    "src/pybamm_verified_loop.py",
    "src/condition_calibration_gate.py",
    "src/sbi_posterior.py",
    "src/sbi_w4_scorer.py",
    "src/prm_scorer.py",                   # real w_1 (§9A.3 PRM ckpt optional)
    # --- data ---
    "data/benchmark/echem_reason_benchmark.jsonl",
    "data/echem_rules/echem_rules_condition_calibration.jsonl",
    # --- ckpt + emission for SBI w_4 ---
    "results/sbi_prior_emit/density_estimator_last.pt",
    "results/sbi_prior_emit/hand_cases_v1.jsonl",
    # --- prompts (imported but not critical for c3_full path) ---
    "prompts/sbi_prior_schema_v1.json",
    "prompts/sbi_prior_config_v1.md",
]


# Runtime artifacts — generated by scripts, not in transcript snapshots.
# If absent, zip builds with a WARN (user can regenerate + patch in later).
OPTIONAL = {
    "data/benchmark/echem_reason_benchmark.jsonl",
    "results/sbi_prior_emit/density_estimator_last.pt",
    "results/sbi_prior_emit/hand_cases_v1.jsonl",
}


def main():
    present, missing_req, missing_opt = [], [], []
    for f in FILES:
        if (ROOT / f).is_file():
            present.append(f)
        elif f in OPTIONAL:
            missing_opt.append(f)
        else:
            missing_req.append(f)
    if missing_req:
        raise SystemExit(
            "missing REQUIRED files (rebuild impossible):\n  "
            + "\n  ".join(missing_req))
    if missing_opt:
        print("WARN: missing OPTIONAL runtime artifacts (zip will lack them):")
        for f in missing_opt:
            print(f"  - {f}")
    with zipfile.ZipFile(OUT, "w", zipfile.ZIP_DEFLATED) as z:
        for f in present:
            z.write(ROOT / f, arcname=f"pvgap_experiment/{f}")
    size_kb = OUT.stat().st_size / 1024
    print(f"wrote {OUT.name}  ({size_kb:.1f} KB, "
          f"{len(present)}/{len(FILES)} files)")


if __name__ == "__main__":
    main()
